- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return[FontArray count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"FontCell" forIndexPath:indexPath];
    
       cell.textLabel.text=[FontArray objectAtIndex:indexPath.row];
    cell.textLabel.font = [UIFont fontWithName:[FontArray objectAtIndex:indexPath.row] size:20.0f];
    
        return cell;
}

